package cz.ryvo.measures;

public class Vector2L {
    private final long x;
    private final long y;

    public Vector2L(final long x, final long y) {
        this.x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public long getY() {
        return y;
    }
}
